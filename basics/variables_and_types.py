# variables_and_types.py

# Strings
name = "Kevin"

# Integers
age = 30

# Floats
height = 5.11

# Booleans
is_learning = True
favorite_language = "C#"  # This variable is currently not assigned a value

print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Learning Python:", is_learning)
print(f"My name is {name}, I am {age} years old, and I am {height} feet tall.")
print(f"My favorite programming language is {favorite_language}.")

# Type checking
print("\n--- Types ---")
print(type(name))
print(type(age))
print(type(height))
print(type(is_learning))